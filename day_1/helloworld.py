print(3 + 4)             # addition(+)
print(3 - 4)             # subtraction(-)
print(3 * 4)             # multiplication(*)
print(3 % 4)             # modulus(%)
print(3 / 4)             # division(/)
print(3 ** 4)            # exponential(**)
print(3 // 4)            # Floor division operator(//)

print('Jordan')
print('Parra')
print('Colombia')
print('I am enjoying 30 days of python')

print(type(10))          # int
print(type(9.8))         # float
print(type(3.14))        # float
print(type(4 - 4j))      # complex
print(type(['Asabeneh', 'Python', 'Finland']))  # list
print(type('Jordan'))    # str
print(type('Parra'))     # str
print(type('Colombia'))  # str

print('Integer: ', 10, 'Float: ', 9.8, 'Complex: ', 4 - 4j)
print('String: ', 'Jordan', 'Boolean: ', True)
print('List: ', ['Asabeneh', 'Python', 'Finland'])
print('Tuple: ', ('Asabeneh', 'Python', 'Finland'))
print('Set: ', {'Asabeneh', 'Python', 'Finland'})
print('Dictionary: ', {'name': 'Jordan', 'country': 'Colombia', 'city': 'Mosquera', 'age': 29})

